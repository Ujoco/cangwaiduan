package com.shuangtu.prison.home.model;

public class ModelApplyOrderMenu {

    private String title;
    private int res;
    private int tagl;

    public ModelApplyOrderMenu(String title, int res, int tagl) {
        this.title = title;
        this.res = res;
        this.tagl = tagl;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getRes() {
        return res;
    }

    public void setRes(int res) {
        this.res = res;
    }

    public int getTagl() {
        return tagl;
    }

    public void setTagl(int tagl) {
        this.tagl = tagl;
    }
}
